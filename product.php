         <script>
          $(document).ready(function ($) {
        	$(".owl-carousel").owlCarousel({
        		loop: true,
        		autoplay: false,
            // autoplaySpeed: 1500,
        		// margin: 10,
        		dots: false,
        		nav: true,
        		items: 3,
            slideTransition: 'linear'
        	});
        
           // Add 'active' class to img tag when item is clicked
           $('.owl-carousel .item').click(function(){
            // Remove 'active' class from all items
            $('.owl-carousel .item').removeClass('active');
            // Add 'active' class to the clicked item
            $(this).addClass('active');
        
            // Remove 'active' class from all img tags
            $('.owl-carousel .item img').removeClass('active');
            // Add 'active' class to the img tag within the clicked item
            $(this).find('img').addClass('active');
          });
        
        	var owl = $(".owl-carousel");
        	owl.owlCarousel();
        	$(".next-btn").click(function () {
        		owl.trigger("next.owl.carousel");
        	});
        	$(".prev-btn").click(function () {
        		owl.trigger("prev.owl.carousel");
        	});
        	$(".prev-btn").addClass("disabled");
        	$(owl).on("translated.owl.carousel", function (event) {
        		if ($(".owl-prev").hasClass("disabled")) {
        			$(".prev-btn").addClass("disabled");
        		} else {
        			$(".prev-btn").removeClass("disabled");
        		}
        		if ($(".owl-next").hasClass("disabled")) {
        			$(".next-btn").addClass("disabled");
        		} else {
        			$(".next-btn").removeClass("disabled");
        		}
        	});
        });
        
          </script>
